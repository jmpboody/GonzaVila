	<h2 align="center">Historia de Gonzávila</h2>
	<article class="media">
	  <div class="media-body">
			<p>En el año de 1999, el Ingeniero José Roberto González Godínez fundó en San Martín Texmelucan, Puebla, un negocio enfocado a la distribución de alimentos balanceados para animales de corral y maíz nixtamalero. El objetivo de Gonzávila ha sido comercializar productos de excelente calidad. En el año 2003, la organización comenzó a ampliar su territorio de distribución de productos, abriendo una nueva sucursal en Apizaco Tlaxcala.</p>

			<p>En el año 2006 se abre la tercera sucursal en el municipio El verde en Puebla, y en el año 2007, se inaugura otra bodega más en Santa María Moyotzingo, Puebla, conocida como Cabaña, en la cual se inicia la manufactura de productos alimenticios con su propio nombre.</p>

			<p>En el año 2012 Gonzávila abre una nueva sucursal en Huejotzingo Puebla, satisfaciendo las necesidades de adquisición de productos alimenticios de otro municipio. Gonzavila es una organización que ha ido creciendo paulatinamente, pues, la calidad de sus productos ha demandado la apertura de otras sucursales en diferentes puntos de los estados de Puebla y Tlaxcala, cumpliendo de esta forma con las expectativas de servicio y calidad de sus clientes.</p>

			<p>La principal característica de Gonzávila es la calidad de sus productos y la atención a sus clientes, la cual se refleja en un trato de amabilidad y respeto.</p>
			
